class Person{
	String name = null;
	String id = null;
	public Person(String name,String id){
		this.name = name;
		this.id = id;
	}
	public void setName(String name)
	{
		self.name = name;
	}
	public String getName()
	{
		return self.name;
	}
	public void setId(String id)
	{
		self.id = id;
	}
	public String getId()
	{
		return self.id;
	}

}